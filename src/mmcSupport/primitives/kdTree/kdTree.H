#include "Tuple2.H"
#include "autoPtr.H"
#include <random>
#include <vector>
#include <queue>
#include <deque>

#include <sstream>
#include <iomanip>

namespace Foam
{

template<class VectorType,int bucketSize=20>
class kdTree 
{
    private:
            // Type of a n-d point to store a reference point in the space.
            // E.g., the high and low point of a box
            using ndPoint = List<scalar>;
            
    public:
        // ================ Structs For K-D Data Output =======================
        
            struct foundParticle
            {
                // Member Variables
                    
                    //- Index in particleList
                    label idx=-1;
                    
                    //- Distance in n-d space
                    scalar dist=-1;

                    //- Distance in physical space
                    List<scalar> disReal;     


                // Constructors 
                    
                    //- Default constructor
                    foundParticle(){};

                    //- Construct with given values, except real dist
                    foundParticle(label Idx ,scalar Dist) 
                    : idx(Idx), dist(Dist) 
                    {}
                
            };
        
        
            // A struct to store the unique particle pairs and their distance
            struct particlePair
            {
                label p1=-1;   // index of particle 1 in the particleList
                label p2=-1;   // index of particle 2 in the particleList
                scalar dist2=0;// distance squared between the two particles
                
                // Constructor
                particlePair() = default;
                
                // Constructor with arguments
                particlePair(label P1, label P2, scalar Dist2)
                : p1(P1),p2(P2),dist2(Dist2) {}
            };
        
    private:
    
        // ================ Structs For K-D Internal Data =====================
            struct Box
            {
                ndPoint lo;   // high value of the coordinates
                ndPoint hi;   // low value of the coordinates on opposing corners

                label splitDim;

                // Indices to the parent and daughter boxes in boxes_
                label parent;
                label left;
                label right;

                // Indices of the particles in the index array
                label particleIndexLo;
                label particleIndexHi;

                // Constructor
                Box
                (
                    ndPoint& Lo,
                    ndPoint& Hi,
                    label Parent,
                    label Left,
                    label Right,
                    label ParticleIndexLo, 
                    label ParticleIndexHi
                )
                : lo(Lo),hi(Hi),parent(Parent),left(Left),right(Right),
                    particleIndexLo(ParticleIndexLo), 
                    particleIndexHi(ParticleIndexHi)
                {}
                
                bool isLeaf() const
                {
                    if (left == -1 && right == -1)
                        return true;
                    return false;
                }
            };


            
            struct compareByDistance
            {
                constexpr bool operator()(foundParticle const &a, foundParticle const &b) const noexcept 
                {
                    return a.dist < b.dist;
                }
            };
            
            
            // Typedef for the priority queue
            typedef std::priority_queue
                <
                    foundParticle,
                    std::vector<foundParticle>,
                    compareByDistance
                > foundParticleQueue;

        // Private Member Variables 

            //- Number of particles stored in one box
            const label bucketsize_=bucketSize;

            //- Reference to the particle list
            const List<VectorType>& particles_;
            
            //- Dimensions of the k-d tree space
            const label DIM_;

            //- Number of particles
            const label nParticles_;
            
            //- Median or averaged based sorting
            const bool medianBasedSorting_;

            //- Switch on or off the normalization of each dimension 
            //  If switched off, norm_ is set to unity and weighting is not 
            //  considered
            const bool normalize_;
            
            //- Weighted factors to normalize distance
            ndPoint norm_;

            //- Indices to the particles
            List<label> particleIndex_;

            //- Vector of boxes
            std::vector<Box> boxes_;
            
        
        // Private Member functions

            //- Construct the tree
            void constructTree(const List<scalar>& weights);

            //- Dereference the particle list
            inline const List<VectorType>& particleList() const 
                {return particles_;} 

            //-iCalculate the dimension with the largest distance to split
            label calcSplitDimension(const Box& box); 

            //- Sort particleIndices for kdTree construction based on a index k
            //  Indicies are sorted so that all values are sorted up to k 
            label sortIndices
            (
                const label pIndexLo, 
                const label pIndexHi, 
                const scalar* coords
            );

            //- Sort particle index based on a threshold value
            //  returns the index of the mid point of particleIndex_ list
            label sortIndicesBasedOnThreshold
            (
                const label pIndexLo, 
                const label pIndexHi,
                const scalar* coords,
                const scalar threshold
            );

            //- Calculate the squared distance in the n-dimensional space
            //  Input are the query vector as VectorType and the point
            //  in the particle list particles_ 
            scalar ndDist(const VectorType& q, const VectorType& p) const;

            //- Find the maximum extend in each dimension 
            Pair<ndPoint> getMinMaxExtend(const List<scalar>& weights);

            //- Calculate the maximum distance from the box boundary to a point
            //  Uses the ndDist() function
            scalar boxToPointDist(const Box& box, const VectorType& q) const;

            //- Shrink the box size to the actual particles containing it
            void shrinkBoxToFit(Box& box);

            //- Find the box that contains particel pI and returns the box index
            label locate(const label pI) const;
            
            //- find nearest particle to particle in a given box
            Tuple2<label,scalar> nearestPtoP_inBox
            (
                const Box& box,         // Reference to the box to search
                const label pI,         // Index of particle 
                const List<bool>& isPaired    // List of already paired particles
            ) const;

            //- Search in box to find points closest to the query

            void searchInBox
            (
                foundParticleQueue& queue,
                const VectorType& q,
                const Box& parentBox,
                const label n
            ) const;

            //- Write particle coordinates as csv file in given range
            void csvWrite(fileName name,label leftIndex,label rightIndex);

            //- Find leaf box and particle index stored in the leaf
            //  Input: Box to search and the std::deque list to which
            //  the found leaf particles are added
            void findLeaf
            (
                const Box& box, 
                std::deque<List<label>>& leafParticleList
            ) const;

    public: 
      
        // Type Def
            typedef std::vector<foundParticle> resList;

        // Constructors
            
            //- Constructor with given particle list
            //  The VectorType can be any object that has operator[](int i) 
            //  access, e.g., a Foam::List<>, Foam::vector2D, Foam::vector...
            //  Input:
            //      particleList    List of point/particle data with positions
            //                      in the n-d space
            //      weights:        To weigh each dimension
            //  medianBasedSorting: Sort the particle in each bin based on 
            //                      their average value or based on median. 
            //      normalize:      Normalize each coordinate direction with
            //                      their maximum and minimum value of each
            //                      dimension of the provided particleList 
            kdTree
            (
                const List<VectorType>& particleList,
                List<scalar> weights = List<scalar>(),
                const bool medianBasedSorting = false,
                const bool normalize = true
            );
            
        // Public Member functions
       
            //- Return n nearest particles as a std::vector<foundParticle> list
            //  The VectorType can be any object that has operator[](int i) access
            resList nNearest
            (
                const VectorType& q,
                const label n
            ) const;
            
            
            //- Return particle indices in each leaf bucket
            //- of the kd-Tree
            List<List<label>> leafParticles() const;
            
            
            //- Creates a list of unique pairs
            //  using a greedy search algorithm
            DynamicList<particlePair> findUniquePairs() const;

            //- Visualize the boxes by writing them out as csv file
            void printTree();
};
};  // End of namespace Foam

#include "kdTree.C"
#endif
